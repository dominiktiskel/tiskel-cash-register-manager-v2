    @Test
    @Transactional
    void putNewInvoice() throws Exception {
        // Initialize the database
        invoiceRepository.saveAndFlush(invoice);

        int databaseSizeBeforeUpdate = invoiceRepository.findAll().size();

        // Update the invoice
        Invoice updatedInvoice = invoiceRepository.findById(invoice.getId()).get();
        // Disconnect from session so that the updates on updatedInvoice are not directly saved in db
        em.detach(updatedInvoice);
        updatedInvoice
            .created(UPDATED_CREATED)
            .payedDate(UPDATED_PAYED_DATE)
            .periodBeginDate(UPDATED_PERIOD_BEGIN_DATE)
            .periodEndDate(UPDATED_PERIOD_END_DATE)
            .number(UPDATED_NUMBER)
            .jsonData(UPDATED_JSON_DATA);
        InvoiceDTO invoiceDTO = invoiceMapper.toDto(updatedInvoice);

        restInvoiceMockMvc
            .perform(
                put(ENTITY_API_URL_ID, invoiceDTO.getId())
                    .contentType(MediaType.APPLICATION_JSON)
                    .content(TestUtil.convertObjectToJsonBytes(invoiceDTO))
            )
            .andExpect(status().isOk());

        // Validate the Invoice in the database
        List<Invoice> invoiceList = invoiceRepository.findAll();
        assertThat(invoiceList).hasSize(databaseSizeBeforeUpdate);
        Invoice testInvoice = invoiceList.get(invoiceList.size() - 1);
        assertThat(testInvoice.getCreated()).isEqualTo(UPDATED_CREATED);
        assertThat(testInvoice.getPayedDate()).isEqualTo(UPDATED_PAYED_DATE);
        assertThat(testInvoice.getPeriodBeginDate()).isEqualTo(UPDATED_PERIOD_BEGIN_DATE);
        assertThat(testInvoice.getPeriodEndDate()).isEqualTo(UPDATED_PERIOD_END_DATE);
        assertThat(testInvoice.getNumber()).isEqualTo(UPDATED_NUMBER);
        assertThat(testInvoice.getJsonData()).isEqualTo(UPDATED_JSON_DATA);
    }

